// --- Tile cache: pre-render each (tileType, paletteId) to an offscreen canvas ---

const tileCache = new Map<string, HTMLCanvasElement>();

const getTileCacheKey = (tile: number, paletteId: PaletteId): string =>
  `${tile}_${paletteId}`;

const renderTileToCache = (tile: number, paletteId: PaletteId): HTMLCanvasElement => {
  const key = getTileCacheKey(tile, paletteId);
  const cached = tileCache.get(key);
  if (cached) return cached;

  const canvas = document.createElement('canvas');
  canvas.width = TILE_SIZE;
  canvas.height = TILE_SIZE;
  const ctx = canvas.getContext('2d')!;
  const palette = PALETTES[paletteId];
  const pattern = TILE_PATTERNS[tile];

  if (pattern) {
    for (let row = 0; row < TILE_SIZE; row++) {
      const patRow = pattern[row];
      if (!patRow) continue;
      for (let col = 0; col < TILE_SIZE; col++) {
        const idx = patRow[col];
        if (idx === undefined) continue;
        const color = palette.colors[idx];
        if (!color) continue;
        ctx.fillStyle = color;
        ctx.fillRect(col, row, 1, 1);
      }
    }
  } else {
    // Fallback: fill with bg
    ctx.fillStyle = palette.colors[4] ?? '#f8f8f0';
    ctx.fillRect(0, 0, TILE_SIZE, TILE_SIZE);
  }

  tileCache.set(key, canvas);
  return canvas;
};

